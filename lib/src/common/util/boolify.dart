bool? tryBoolify(Object object) {
  if (object is bool) return object;

  if (object is num) {
    return switch (object) {
      1 => true,
      0 => false,
      _ => null,
    };
  }

  if (object is String) {
    final s = object.trim().toLowerCase();
    return switch (s) {
      '0' => false,
      '1' => true,
      _ => bool.tryParse(s),
    };
  }

  return null;
}
